package io.hostilerobot.ceramicrelief.texture.data_tex;

public class TFace {
    private final int v1;
    private final int v2;
    private final int v3;

    public TFace(int v1, int v2, int v3) {
        this.v1 = v1;
        this.v2 = v2;
        this.v3 = v3;
    }

    public int getV1() {
        return v1;
    }
    public int getV3() {
        return v3;
    }
    public int getV2() {
        return v2;
    }
}
